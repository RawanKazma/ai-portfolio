def create_data_splits(root_data_path):
    """
    Performs a class-disjoint split (80 train classes, 20 test classes)
    and physically copies the images into new directory structures.
    """
    random.seed(RANDOM_SEED)

    # Output Directories
    train_path = os.path.join(root_data_path, TRAIN_OUTPUT_FOLDER)
    test_path = os.path.join(root_data_path, TEST_OUTPUT_FOLDER)

    if os.path.exists(train_path) and os.path.exists(test_path):
      print("Split already happened for you")
      return

    # 1. Determine all unique class names
    class_folders_path = os.path.join(root_data_path, TRAIN_SPLIT_FOLDER)
    if not os.path.exists(class_folders_path):
        raise FileNotFoundError(f"Source folder not found: {class_folders_path}. Check ROOT_DATA_PATH.")

    all_entries = os.listdir(class_folders_path)
    all_classes = sorted([entry for entry in all_entries if os.path.isdir(os.path.join(class_folders_path, entry)) and not entry.startswith('.')])

    if len(all_classes) != TOTAL_CLASSES:
        print(f"WARNING: Found {len(all_classes)} classes, but expected {TOTAL_CLASSES}. Proceeding with found classes.")

    # 2. Perform random class split
    shuffled_classes = all_classes[:]
    random.shuffle(shuffled_classes)

    train_classes = set(shuffled_classes[:TRAIN_COUNT])
    test_classes = set(shuffled_classes[TRAIN_COUNT:])

    os.makedirs(train_path, exist_ok=True)
    os.makedirs(test_path, exist_ok=True)

    # 3. Copy files to new split folders
    splits_counts = {'train': 0, 'test': 0}

    class_to_output_path = {}
    for cl in train_classes: class_to_output_path[cl] = os.path.join(train_path, cl)
    for cl in test_classes: class_to_output_path[cl] = os.path.join(test_path, cl)

    # Loop through the two source data folders to gather all images
    for data_folder_name in [TRAIN_SPLIT_FOLDER, TEST_SPLIT_FOLDER]:
        current_data_path = os.path.join(root_data_path, data_folder_name)

        for class_label in all_classes:
            output_class_path = class_to_output_path.get(class_label)
            if not output_class_path:
                continue

            # Create the class folder in the new split directory
            os.makedirs(output_class_path, exist_ok=True)

            # Find all image files in the source class folder
            class_path_source = os.path.join(current_data_path, class_label)

            exts = ["JPEG"]
            image_paths = []
            for ext in exts:
                image_paths.extend(glob(os.path.join(class_path_source, f"*.{ext}")))

            # Copy the files
            set_name = 'train' if class_label in train_classes else 'test'
            for path in image_paths:
                try:
                    # Use the base filename to avoid path conflicts
                    destination_path = os.path.join(output_class_path, os.path.basename(path))
                    shutil.copy2(path, destination_path)
                    splits_counts[set_name] += 1
                except Exception as e:
                    print(f"Error copying file {path}: {e}")

