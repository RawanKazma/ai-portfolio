class ContrastiveCustomCNN(nn.Module):
    def __init__(self):
        super(ContrastiveCustomCNN, self).__init__()

        # Block 1:
        # (BS, 3 x 224 x 224) (BS = Batch size)
        self.conv_block1 = nn.Sequential(nn.Conv2d(in_channels=3, out_channels=64, kernel_size=3, stride=1, padding=1),
                                    nn.BatchNorm2d(64),
                                    nn.ReLU(),
                                    nn.MaxPool2d(kernel_size=2, stride=2))

        # Block 2:
        # (BS x 128 x 112 x 112)
        self.conv_block2 = nn.Sequential(nn.Conv2d(in_channels=64, out_channels=128, kernel_size=3, stride=1, padding=1),
                                    nn.BatchNorm2d(128),
                                    nn.ReLU(),
                                    nn.MaxPool2d(kernel_size=2, stride=2))

        # Block 3:
        # (BS x 256 x 56 x 56)
        self.conv_block3 = nn.Sequential(nn.Conv2d(in_channels=128, out_channels=256, kernel_size=3, stride=1, padding=1),
                                    nn.BatchNorm2d(256),
                                    nn.ReLU(),
                                    nn.MaxPool2d(kernel_size=2, stride=2))

        # Block 4:
        # (BS x 256 x 28 x 28)
        self.conv_block4 = nn.Sequential(nn.Conv2d(in_channels=256, out_channels=256, kernel_size=3, stride=1, padding=1),
                                    nn.BatchNorm2d(256),
                                    nn.ReLU(),
                                    nn.MaxPool2d(kernel_size=2, stride=2))

        # Global average pooling:
        # (BS x 256 x 1 x 1)
        self.global_avg_pool = nn.AdaptiveAvgPool2d((1, 1))

        # Full connected layer
        self.fc = nn.Linear(256, 256)

        self.final_activation = nn.Sigmoid()

    def forward(self, x):
        x = self.conv_block1(x)
        x = self.conv_block2(x)
        x = self.conv_block3(x)
        x = self.conv_block4(x)

        x = self.global_avg_pool(x)
        x = x.view(x.size(0), -1) # (BS x 512)

        x = self.fc(x)

        embedding = self.final_activation(x)

        normalized_embedding = F.normalize(embedding, p=2, dim=1)

        return normalized_embedding

