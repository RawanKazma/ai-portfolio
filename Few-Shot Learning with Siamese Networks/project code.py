def contrastive_loss(embedding_1, embedding_2, label, margin=1.0):
    # label=0 if same class, 1 if different
    distance = F.pairwise_distance(embedding_1, embedding_2) # Euclidian distance between vectors

    loss_same = (1 - label) * torch.pow(distance, 2)
    loss_diff = label * torch.pow(torch.clamp(margin - distance, min=0.0), 2)

    return torch.mean(loss_same + loss_diff) #, dim = 0

