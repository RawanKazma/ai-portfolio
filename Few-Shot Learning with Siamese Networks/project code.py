class ContrastiveDataset(Dataset):
    def __init__(self, root_dir, transform=None):
        # ImageFolder reads your folder structure automatically
        self.base_dataset = datasets.ImageFolder(root=root_dir, transform=transform)
        self.labels = np.array(self.base_dataset.targets)

        # Group indices by class
        self.class_indices = {}
        for idx, label in enumerate(self.labels):
            self.class_indices.setdefault(label, []).append(idx)

        self.unique_classes = list(self.class_indices.keys())

    def __len__(self):
        return len(self.base_dataset)

    def __getitem__(self, index):
        # Anchor
        img1, label1 = self.base_dataset[index]

        if random.random() < 0.5:
            # POSITIVE pair (same class): 1
            pair_label = torch.tensor(1.0)
            idx2 = index
            while idx2 == index:
                idx2 = random.choice(self.class_indices[label1])
        else:
            # NEGATIVE pair (different class): 0
            pair_label = torch.tensor(0.0)
            neg_class = random.choice([c for c in self.unique_classes if c != label1])
            idx2 = random.choice(self.class_indices[neg_class])

        img2, _ = self.base_dataset[idx2]

        return img1, img2, pair_label

