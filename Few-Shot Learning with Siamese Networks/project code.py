class TripletDataset(Dataset):
    def __init__(self, root_dir, transform=None):
        self.base_dataset = datasets.ImageFolder(root=root_dir, transform=transform)
        self.labels = np.array(self.base_dataset.targets)

        self.class_indices = {}
        for idx, label in enumerate(self.labels):
            self.class_indices.setdefault(label, []).append(idx)

        self.unique_classes = list(self.class_indices.keys())

    def __len__(self):
        return len(self.base_dataset)

    def __getitem__(self, index):
        # 1. Anker (A)
        img_A, label_A = self.base_dataset[index]

        # 2. Positief (P)
        # P moet van dezelfde klasse zijn als A, maar een andere afbeelding.
        positive_indices = self.class_indices[label_A]
        valid_positive_indices = [i for i in positive_indices if i != index]
        idx_P = random.choice(valid_positive_indices)
        img_P, _ = self.base_dataset[idx_P]


        # 3. Negatief (N)
        # N moet van een andere klasse zijn dan A.
        neg_class = random.choice([c for c in self.unique_classes if c != label_A])
        idx_N = random.choice(self.class_indices[neg_class])
        img_N, _ = self.base_dataset[idx_N]

        # Retourneer het triplet (Anker, Positief, Negatief)
        return img_A, img_P, img_N

